package Strings;

public class BooleanEquals {
    public static void main(String[] args) {
        Boolean b1 = "Harsh".equals("Harsh");
        Boolean b2 = "Pandey".equals("pandey");
        Boolean b3 = "Age".equalsIgnoreCase("AGE"); //Boolean ignore case
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
    }
}